public class HaromszogException extends Exception {
    public HaromszogException() {
    }

    public HaromszogException(String message) {
        super(message);
    }

    public HaromszogException(String message, Throwable cause) {
        super(message, cause);
    }

    public HaromszogException(Throwable cause) {
        super(cause);
    }

    public HaromszogException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
